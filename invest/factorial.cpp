#include <iostream>
#include <stdio.h>
#include <stdlib.h>
using namespace std;

double factorial (long n) {
	double F = 1.0;	// holds the factorial value

	for (int i = 1; i <= n; i++) {
		F = F * (double)i;	// factorial multiplies i into the previous factorial
	}
	
	return F; 
}

double factOddsOnly (long n) {
	double F = 1.0;	// holds the factorial value

	for (int i = 1; i <= n; i++) {
		if (i % 2 != 0) {	// only odd numbers are used to contribute the factorial result
			F = F * (double)i;	// factorial multiplies i into the previous factorial
		}
	}
	
	return F; 
}

int main() {
	long n = 0;	// n holds input value

	cout << "Please input a number: ";
	cin >> n;
	system("clear"); 
	if (n >= 0) {
		cout << n << "! = ";
		cout << factorial (n) << endl; 
	} else {
		cout << "Factorials can't be negative!" << endl;
	}

	return 0;
}

